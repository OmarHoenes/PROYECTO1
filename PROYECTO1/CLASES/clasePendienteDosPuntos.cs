using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace PROYECTO1.CLASES
{
    internal class clasePendienteDosPuntos
    {
        public double claseDosPuntos(double y2, double y1, double x2, double x1)
        {
            return (y2 - y1) / (x2 - x1);
        }



    }
